print(list(range(5)))        # [0,1,2,3,4]
print(list(range(5,10)))     # [5,6,7,8,9]
print(list(range(5,20,3)))   # [5,8,11,14,17]
print(list(range(20,5,-1)))  # [20,19,...,6]
print(list(range(20,5,-3)))  # [20,17,14,11,8]
print(list(range(10,5)))     # []
print(list(range(0)))        # []
print(list(range(10,101,10)))# [10,20,...,100]
print(list(range(10,-1,-1))) # [10,9,...,0]
print(list(range(-3,4)))     # [-3,-2,-1,0,1,2,3]
print(list(range(0,10,1)))   # [0,1,2,...,9]